const translater = (language) => {
  Object.keys(languages).forEach((selector) => {
    if (document.querySelector(selector)) {
      selector.endsWith('__input')
        ? (document.querySelector(selector).placeholder =
            languages[selector][0][language])
        : document
            .querySelectorAll(selector)
            .forEach(
              (element, position) =>
                (element.innerHTML =
                  languages[selector][position]?.[language] ??
                  languages[selector][0][language])
            );
    }
  });
};

